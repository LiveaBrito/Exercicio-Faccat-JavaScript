// Descrição   : Solicita a base e altura e exibe a sua area

alert("Solicitando a base e altura de um triângulo e exibindo a sua area")

let base = Number(prompt("Digite o valor da base: "))
let altura = Number(prompt("Digite o valor da altura: "))

let area = (base * altura)/2

alert(`A área do triângulo é: ${area} cm²`)

