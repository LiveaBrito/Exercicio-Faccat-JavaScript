//Descrição: Solicitar a altura e largura do retângulo e calcular sua área

alert("Solicitar altura e largura de um retângulo e calcular sua área")

let altura = Number(prompt("Digite o valor da  altura: "))
let largura = Number(prompt("Digite o valor da largura: "))

let area = altura * largura

alert(`O valor da area do retangulo é ${area} cm²`)